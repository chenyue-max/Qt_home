/////////////////////////////////////////////////////////////////////
//用MF522计算CRC16函数
//CRC协处理器的16位长计算多项式固定为：x16+x12+x5+1,符合ISO/1EC14443协议
/////////////////////////////////////////////////////////////////////
void CalulateCRC(u8 *pIn ,u8   len,u8 *pOut )
{
    u8   i,n;
    ClearBitMask(DivIrqReg,0x04);//清RC522寄存器位
    WriteRawRC(CommandReg,PCD_IDLE);//写RC522寄存器位，取消当前命令
    SetBitMask(FIFOLevelReg,0x80);//置RC522寄存器位，清除ErrReg的标志位
    for (i=0; i<len; i++)
    {   WriteRawRC(FIFODataReg, *(pIn +i));  }
    WriteRawRC(CommandReg, PCD_CALCCRC);//CRC计算
    i = 0xFF;
    do 
    {
        n = ReadRawRC(DivIrqReg);
        i--;
    }
    while ((i!=0) && !(n&0x04));//当CRCIRq所有数据被处理完毕该位置位
    pOut [0] = ReadRawRC(CRCResultRegL);//显示计算出来的CRC值
    pOut [1] = ReadRawRC(CRCResultRegM);
}


