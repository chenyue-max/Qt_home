//M1卡分为16个扇区，每个扇区由4块（块0，块1，块2，块3）组成
//我们也将16个扇区的64个块按绝对地址编号0~63
//第0扇区的块0（即绝对地址0块），它用于存放产商代码，已经固化，不可更改
//每个扇区的块0，块1，块2为数据块，课用于存放数据
//每个扇区的块3为控制块（绝对地址块3、7、11....），包括了密码A，存取控制、密码B

void delay_ns(u32 ns)
{
  u32 i;
  for(i=0;i<ns;i++)
  {
    __nop();
    __nop();
    __nop();
  }
}

u8 SPIWriteByte(u8 Byte)
{
	u16 retry = 0;
	while((SPI1->SR&0X02)==0)		//等待发送区空	0表示send_buffer非空
	{
			retry++;
			if(retry == 0XFFFE) return 0; 
	}
	SPI1->DR=Byte;	 							//发送一个byte 
	retry = 0;
	while((SPI1->SR&0X01)==0)		//等待接收完一个byte  0表示Receive_buffer空
	{
			retry ++;
			if(retry == 0XFFFE) return 0;
	}
	delay_ns(30);									//务必延时30ns以便使SPI1进入空闲模式，拉低时钟线，否则将无法通信
	return SPI1->DR;          	  //返回收到的数据			
}

//配置SPI1	
void SPI1_Init(void)
{	 
	RCC->APB2ENR|=1<<12;//SPI1时钟使能 
	RCC->APB2ENR|=1<<3;//PORTB时钟使能
	RCC->APB2ENR|=1<<2;//PROTA时钟使能
	//RCC->APB2ENR|=1<<6;//PORTE时钟使能
	
	GPIOA->CRL&=0X0000FFFF;		 
	GPIOA->CRL|=0XBBB30000;		 //PA5 6 7 复用  PA4 推挽输出 片选
	GPIOA->ODR|=0X7<<5;			 //初始化高电平
	GPIOB->CRL&=0XFFFFFF0F;		 
	GPIOB->CRL|=0X00000030;		 //PB1 推挽输出 复位
	
	SPI1->CR1|=0<<10;//全双工模式	
	SPI1->CR1|=1<<9; //软件nss管理
	SPI1->CR1|=1<<8;  
	
	SPI1->CR1|=1<<2; //SPI主机
	SPI1->CR1|=0<<11;//8bit数据格式	
	//SPI1->CR1|=1<<1;//空闲模式下SCK为1 CPOL=1
	//SPI1->CR1|=1<<0;//数据采样从第2个时间边沿开始,CPHA=1  
	SPI1->CR1&=~0x02;//空闲模式下SCK为0 CPOL=0
	SPI1->CR1&=~0x01;//数据采样从第一个时间边沿开始,CPHA=0  
	SPI1->CR1|=7<<3; //Fsck=Fcpu/4
	SPI1->CR1|=0<<7; //MSBfirst   
	SPI1->CR1|=1<<6; //SPI设备使能
}

//RC522射频卡模块初始化
