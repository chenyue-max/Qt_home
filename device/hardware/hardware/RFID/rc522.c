//返    回：读出的值
/////////////////////////////////////////////////////////////////////
u8 ReadRawRC(u8   Address)
{
    u8   ucAddr;
    u8   ucResult=0;
	
   ucAddr = ((Address<<1)&0x7E)|0x80;//地址最后一位要补零
	SET_SPI_CS;
	CLR_SPI_CS;
	SPIWriteByte(ucAddr);
	ucResult=SPIReadByte();
	SET_SPI_CS;
    return ucResult;
}


